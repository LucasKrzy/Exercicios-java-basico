package exercicios;


/**
 * Programa para imprimir os 10 primeiros números inteiros maiores do que 100
 * @author Lucas Pedroso Krzyzanowski
 * @since 10/02/2021
 */
public class Exercicio68 {
	/*
	 * Método principal para executar a classe
	 */
	public static void main(String[] args) {

		// exibindo os 10 priemiros valores maiores do que 100
		for (int i = 1; i <= 10; i++) {
			System.out.println("Numero: " + (i + 100));
		}
	}

}
