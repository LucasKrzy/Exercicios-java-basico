package exercicios;


/**
 * Programa para imprimir a tabuado do 8
 * @author Lucas Pedroso Krzyzanowski
 * @since 10/02/2021
 */
public class Exercicio70 {
	/*
	 * Método principal para executar a classe
	 */
	public static void main(String[] args) {

		// exibindo a tabuada do 8
		for (int i = 1; i <= 10; i++) {
			System.out.println("8 x "+ i + " = " + (i * 8));
		}
	}

}
