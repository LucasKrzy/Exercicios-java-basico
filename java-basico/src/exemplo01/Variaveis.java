package exemplo01;

/**
 * Programa para demonstrar utilização de variáveis
 * @author lpedrosokr
 *@since 04/02/2021
 */
public class Variaveis {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {
		// declarando uma variavel do tipo inteiro
		int idade;
		
		// declarando uma variavel do tipo string
		
		String nome;
		
		// declarando uma variavel do tipo double
		double valor;
		
		// atribuindo valores para as variaveis
		
		idade = 25;
		nome = "Lucas Pedroso Krzyzanowski";
		valor = 13.159;
		
		// exibindo valores das variáveis no console
		
		System.out.println("Idade: " + idade);
		System.out.println("Nome: " + nome);
		System.out.println("Valor: " + valor);

	}

}
