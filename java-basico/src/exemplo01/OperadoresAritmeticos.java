package exemplo01;

/**
 * Programa para demonstrar operações aritméticas
 * @author lpedrosokr
 * @since 05/02/2021
 */
public class OperadoresAritmeticos {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {
		// Declarando variaveis
		int adicao;
		int subtracao;
		int multiplicacao;
		double divisao;
		int modulo;
		
		// exemplo de adição
		adicao = 5 + 3;
		
		// exemplo de subtração
		subtracao = 5 - 2;
		
		// exemplo de multiplicação
		multiplicacao = 8 * 3;
		
		// exemplo de divisao
		divisao = 10 / 3;
		
		// exemplo de módulo
		modulo = 6 % 4;
		
		// exibindo o resultado no console
		
		System.out.println("5 + 3 = " + adicao);
		System.out.println("5 - 2 = " + subtracao);
		System.out.println("8 * 3 = " + multiplicacao);
		System.out.println("10 / 3 = " + divisao);
		System.out.println("6 % 4 = " + modulo);
				

	}

}
